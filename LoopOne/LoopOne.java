/* write a java program that will print hello Java 5 times
 *
 */
 
 public class LoopOne
 {
 	public static void main(String [] args)
 	{
 		int count;
 		
 		count = 1;      	//starting action
 		
 		while(count <= 5) 	//ending action
 		{
 			System.out.println(count + ". hello java");
 			count = count + 1;
 		}

 			
 				
 		
 	}
 }